public class Ciclo {
    private int idciclo;
    private int ciclo;


    public Ciclo() {
    }

    public Ciclo(int idciclo, int ciclo) {
        this.idciclo = idciclo;
        this.ciclo = ciclo;

    }

    public int getIdciclo() {
        return idciclo;
    }

    public void setIdciclo(int idciclo) {
        this.idciclo = idciclo;
    }

    public int getCiclo() {
        return ciclo;
    }

    public void setCiclo(int ciclo) {
        this.ciclo = ciclo;
    }


}
